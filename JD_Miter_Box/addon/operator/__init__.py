import bpy

from .align import MB_OT_ALIGN

classes = (
    MB_OT_ALIGN,
)


def register_operators():
    from bpy.utils import register_class
    for cls in classes:
        register_class(cls)


def unregister_operators():
    from bpy.utils import unregister_class
    for cls in reversed(classes):
        unregister_class(cls)
